package org.kaznalnrprograms.Accounting.reffers.Models;

public class ReffersAll {
    int id;
    String name;
    int codeLen;
    int isCodeDigit;


    public int getId() { return id; }
    public void setId(int id) { this.id = id; }
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public int getCodeLen() {        return codeLen;    }
    public void setCodeLen(int codeLen) {        this.codeLen = codeLen;    }
    public int getIsCodeDigit() {        return isCodeDigit;    }
    public void setIsCodeDigit(int isCodeDigit) {        this.isCodeDigit = isCodeDigit;    }
}
