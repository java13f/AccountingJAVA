package org.kaznalnrprograms.Accounting.OrderRepairs.Models;

public class ListparamsModel
{
    private int id;
    private String name;
    private int nom;
    private int strict;

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getNom()
    {
        return nom;
    }

    public void setNom(int nom)
    {
        this.nom = nom;
    }

    public int getStrict()
    {
        return strict;
    }

    public void setStrict(int strict)
    {
        this.strict = strict;
    }
}
